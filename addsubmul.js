// find addition,subtraction,multiplication and division of numbers using functions


function add(num1,num2){
    return num1+num2;
}
console.log(add(5,5));


function sub(num1,num2){
  return num1-num2;
}

console.log(sub(5,6));


function mul(num1,num2){
  return num1*num2
}

console.log(mul(5,6));

function div(num1,num2){
  return num1/num2;
}

console.log(div(10,5));


