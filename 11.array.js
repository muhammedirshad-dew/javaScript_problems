/// Array - [  ]
//push - add data to last
//pop - remove data from last
//shift - remove data from first
//unshift - add data to first

//Array is a linear data structure
//It has a sequential data storing format


var abc = ["me","rahul","swamed"]


// push

abc.push("angel")
console.log(abc)


abc.pop()
console.log(abc)

abc.shift()
console.log(abc)


abc.unshift("me")
console.log(abc)


